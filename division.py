"""
This module contains a function to perform division of two numbers.

The `division` function takes in two numbers and returns the result of
division the second number to the first.
"""
def division(first_number, second_number):
    """
    Divide first_number to second_number.

    Args:
        first_number (int or float): The number to be divided.
        second_number (int or float): The number to divide.

    Returns:
        int or float: The result of division `first_number` to `second_number`.
    """
    return first_number / second_number
