"""
This module contains a function to perform multiplication of two numbers.

The `addition` function takes in two numbers and returns the result of
additioning the second number to the first.
"""
def multiplication(first_number, second_number):
    """
    Multiplies first_number to second_number.

    Args:
        first_number (int or float): The number to be multiplied to.
        second_number (int or float): The number to multiply.

    Returns:
        int or float: The result of multiplying `first_number` to
        `second_number`.
    """
    return first_number * second_number
