"""
This module contains a function to perform exponetiation.

The `exponentiation` function takes in two numbers and returns the result of
exponentiationing the first number to the second.
"""
def exponentiation(first_number, second_number):
    """
    Exponentiate first_number to second_number.

    Args:
        first_number (int or float): The number to be exponentiated.
        second_number (int or float): The exponent.

    Returns:
        int or float: The result of exponentiation `first_number` to
        `second_number`.
    """
    return first_number ** second_number
