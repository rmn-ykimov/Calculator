"""
This module contains a function to perform subtraction of two numbers.

The `subtraction` function takes in two numbers and returns the result of
subtracting the second number from the first.
"""
def subtraction(first_number, second_number):
    """
    Subtracts second_number from first_number.

    Args:
        first_number (int or float): The number to be subtracted from.
        second_number (int or float): The number to subtract.

    Returns:
        int or float: The result of subtracting `second_number` from
        `first_number`.
    """
    return first_number - second_number
