"""
This module contains a function to perform addition of two numbers.

The `addition` function takes in two numbers and returns the result of
additioning the second number to the first.
"""
def addition(first_number, second_number):
    """
    Adds second_number to first_number.

    Args:
        first_number (int or float): The number to be added to.
        second_number (int or float): The number to add.

    Returns:
        int or float: The result of additioning `second_number` to
        `first_number`.
    """
    return first_number + second_number
